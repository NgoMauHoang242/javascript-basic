//Các phéo so sánh

let a = 1;
let b = 3;
let c = "3";
console.log(typeof a);
console.log(typeof b);
console.log(typeof c);

//các phép so sánh giôngs toán học
console.log(a > b);
console.log(a < b);
console.log(a <= b);
console.log(a >= b);
console.log(a != b);
console.log(c != b); //lưu ý

// so sánh == (không quan tâm đến kiểu của biến)
console.log(a == b);
console.log(c == b);
//So sánh tuyệt đối (===) So sánh giá trị và kiểu dl
console.log(a === b);
console.log(c === b);

Console.log("Phần 2");
console.log(a != b);
console.log(c != b); //không quan tâm đến kiểu của biến

console.log(a !== b);
console.log(c !== b); //So sánh giá trị và kiểu dl
