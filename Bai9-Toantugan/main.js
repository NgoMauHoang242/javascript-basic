//Toan tử gán
// Toán tử gán = gans value cho biến
let x = 10; //gán x =10
let y = 5; // gán y = 5
let z = x + y; //gán z = x + y
console.log(x);
console.log(y);
console.log(z);

//Gán +=;-=;*=;/=:%=
console.log("Kết qủa gán +=, -=, *=,/=,%=");
let m = 10;
m += 5; //m = m +5
console.log(m);

let n = 8;
n -= 3; // n = n - 3
console.log(n);

let p = 6;
p *= 2; //p = p * 2
console.log(p);

let q = 9;
q /= 3; // q = q / 3
console.log(q);

let r = 4;
r %= 3; //r = r % 3
console.log(r);
