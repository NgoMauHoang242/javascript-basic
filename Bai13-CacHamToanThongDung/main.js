// Các hàm toán học thông dụng
// Hàm `Math.sqrt()`
let a = Math.sqrt(4);
console.log(a);

//Hàm `Math.pow(base , exponent)`
let b = Math.pow(2, 3);
console.log(b);

//Hàm Math.abs()
let c = Math.abs(-10);
console.log(c);

//Hàm Math.ceil() và Math.floorr()
let d = Math.floor(5.01);
console.log(d);

//Hàm Math.round(), từ 0.5 tròn lên thành 1
let e = Math.round(7.4999);
console.log(e);

//Làm tròn x chữ số phần đơn vị
let f = 3.14159;
let fRound = f.toFixed(2);
console.log(fRound);
console.log(typeof fRound);

//Hàm Math.min và Math.max
let h = Math.min(2, 3, 5, 4, 100);
console.log(h);
